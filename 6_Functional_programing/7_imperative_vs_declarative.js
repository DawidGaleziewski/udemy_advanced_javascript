// Imperative - code that tell machine what to do and how to do it. Example: wall over there, turn left grab that glass of water and bring it to me.
//  Declarative - code that tells what to do and what should happen. It does not tell compuer how to do it.. Example: "give me that glass of water"

// Computers by default are better at imperative tasks
//  The higher level the language the more declarative it becomes

// Imperative loop
for(let i = 0; i < 1000; i++){
    console.log(i);
}

// Declarative loop
[1,3,4,5,6].forEach(item => console.log(item));

